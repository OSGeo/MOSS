C
C      CHANGES COORIDINATES FROM METERS TO INCHES AND FACTORS
C      PRODUCT BY SCALE
C
C      XX=INPUT COORDINATE IN METERS
C      XC=OUTPUT COORDINATE IN INCHES
C     ORG=MINIMUM VALUE OR ORIGION FOR COORDINATE
C   SCALE=OUTPUT MAP SCALE
C
      SUBROUTINE cptrns(xx,xc,org,scale)
      xc = ((xx-org)*39.37)/scale
      RETURN
      END
