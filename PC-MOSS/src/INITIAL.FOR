      SUBROUTINE initial
CC
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
CC
CC NAME : SUBROUTINE INITIAL
CC
CC FUNCTION : TO READ IN THE STATUS OF THE ASSOCIATED ATTRIBUTES DATA
CC            BASE.
CC
CC PARAMETERS :
CC
CC FILES : USES THE DATA BASE NAMED FILE.ATTRIBUTES.
CC
CC ROUTINES REQUIRED : SUBROUTINE WRBLK AND SUBROUTINE RDBLK
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
      COMMON /unit  / chanel , atrbte , polygon , adress
CC
      INTEGER chanel , atrbte , polygon , adress , record(128)
CC
      adress = 203
CC
CC
CC READ FROM LOCATION 1 OF THE DATA BASE TO RETRIVE THE NUMBER OF
CC ATTRIBUTES( ATRBTE ) IN THE DATA BASE AND THE NUMBER OF
CC POLYGONS( POLYGON ) IN EACH ATTRIBUTE SET.
CC
      CALL rdblk(chanel,1,record,1,ier)
CC
      atrbte = record(1)
      polygon = record(2)
CC
CC IF THE NUMBER OF ATTRIBUTES IS ZERO THEN RETURN, ELSE CALCULATE THE
CC LOCATION( ADRESS ) IN THE DATA BASE WHERE THE NEXT SET OF POLYGON
CC VALUES SHALL BE PLACED.
CC
      IF ( atrbte.NE.0 ) THEN
         locate = atrbte + 1
         CALL rdblk(chanel,locate,record,1,ier)
         adress = record(86) + record(87)
      ENDIF
CC
      RETURN
      END
