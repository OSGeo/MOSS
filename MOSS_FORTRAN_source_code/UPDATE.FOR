        SUBROUTINE UPDATE
CC
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
CC
CC NAME : SUBROUTINE UPDATE
CC
CC FUNCTION : TO ZERO OUT THE POLYGON VALUES OF A ATTRIBUTE SET OF INTEREST
CC            AND INSERT NEW POLYGON VALUES.
CC
CC PARAMETERS :
CC
CC FILES :
CC
CC ROUTINES REQUIRES : SUBROUTINE RETRVL, SUBROUTINE DIGET, SUBROUTINE
CC                      REALS, SUBROUTINE LETERS
CC SYSTEM DEPENDENCE :
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
CC
          COMMON/ UNIT / CHANEL, ATRBTE, POLYGON, ADRESS
CC
          INTEGER CHANEL, ATRBTE, POLYGON, ADRESS, RECORD( 128 ),
     -             REPEAT, TIMES
CC
          LOGICAL FOUND
CC
            CALL RETRVL( FOUND, RECORD )
CC
CC RETRIEVE THE ATTRIBUTE RECORD OF INTEREST
CC
            IF( .NOT. FOUND ) GO TO 4000
CC
CC RECORD( 86 ) CONTAINS THE LOCATION IN THE DATA BASE WHERE THE
CC FIRST RECORD OF POLYGON VALUES ARE STORED FOR THE  ATTRIBUTE
CC  OF INTEREST IS STORED
CC
              LOCATE = ADRESS
              ADRESS = RECORD( 86 )
CC
CC RECORD( 84 ) IS THE CODE VALUE OF THE DATA TYPE OF THE ATTRIBUTE VALUES.
CC 1 IF INTEGER, 2 IF REAL AND 3 IF IT IS A CHARACTER STRING.
CC
                GO TO( 1000, 2000, 3000 ) RECORD( 84 )
CC
1000            CONTINUE
                  CALL DIGET
                  ADRESS = LOCATE
                  RETURN
CC
CC
2000            CONTINUE
                  CALL REALS
                  ADRESS = LOCATE
                  RETURN
CC
3000            CONTINUE
                  CALL LETERS( RECORD( 85 ) )
                  ADRESS = LOCATE
                  RETURN
CC
4000      CONTINUE
      WRITE(NPRNT,*)' ATTRIBUTE NOT FOUND. PROGRAM TERMINATES. '
            RETURN
        END
